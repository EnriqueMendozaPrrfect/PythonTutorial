# () Parentesis
# / y * División y multiplicación
# + y -	Suma y resta
# La forma en la que se resuelven es de izquierda a derecha

suma = 5 + 6
print('el valor de suma es:', suma)

resta = 5 - 2
print('el valor de resta es:', resta)

multiplicacion = 3 * 4
print('el valor de multiplicacion es:', multiplicacion)

division = 12 % 3
print('el valor de division es:', division)

parentesis = (5 + 3) * 2
print('el valor de parentesis es:', parentesis)

